package com.kajsa.demo;

public class Player {


    // Declared two variables
    String name;
    int score;


    // Methods, getters and setters
    // This makes it possible to store the players and points
    public void setNames(String namn)
    {
        name = namn;
    }
    public String getNames()
    {
        return name;
    }
    public void setScore(int points)
    {
        score = points;
    }
    public int getScore(){
        return score;
    }


}
